@Entity
@Table(name = "item_payment_methods")
@JsonIgnoreProperties({"hibernateLazyInitializer", "handler"})
public class ItemPaymentMethods {
    private long id;
    private long itemProductId;
    private long paymentMethodId;
    private int number;
    private int price;
    private Timestamp createdAt;
    private Timestamp updatedAt;

    @Id
    @Column(name = "id", nullable = false)
    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    @Basic
    @Column(name = "item_product_id", nullable = false)
    public long getItemProductId() {
        return itemProductId;
    }

    public void setItemProductId(long itemProductId) {
        this.itemProductId = itemProductId;
    }

    @Basic
    @Column(name = "payment_method_id", nullable = false)
    public long getPaymentMethodId() {
        return paymentMethodId;
    }

    public void setPaymentMethodId(long paymentMethodId) {
        this.paymentMethodId = paymentMethodId;
    }

    @Basic
    @Column(name = "number", nullable = false)
    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    @Basic
    @Column(name = "price", nullable = false)
    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    @Basic
    @Column(name = "created_at", nullable = true)
    public Timestamp getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Timestamp createdAt) {
        this.createdAt = createdAt;
    }

    @Basic
    @Column(name = "updated_at", nullable = true)
    public Timestamp getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(Timestamp updatedAt) {
        this.updatedAt = updatedAt;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        ItemPaymentMethods that = (ItemPaymentMethods) o;
        return id == that.id && itemProductId == that.itemProductId && paymentMethodId == that.paymentMethodId && number == that.number && price == that.price && Objects.equals(createdAt, that.createdAt) && Objects.equals(updatedAt, that.updatedAt);
    }

    @Override
    public int hashCode() {
        return Objects.hash(id, itemProductId, paymentMethodId, number, price, createdAt, updatedAt);
    }
}
